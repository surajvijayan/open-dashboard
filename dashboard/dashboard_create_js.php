function create_js($plane_name)
{
	global $planes;
	$js_file = "dashboard_" . $plane_name . ".js";
	$fd = fopen($js_file,"w");

	$found = FALSE;
	foreach($planes as $plane)
	{
		if($plane_name == $plane["name"])
		{
			$found = TRUE;
			if (!isset($plane["config"]))
			{
				print "'config' token is not defined in plane:$plane_name inside vars.inc file!\n";
				exit(0);
			}
		    if (!file_exists($plane["config"]))
            {
                print "Can't open:" . $plane["config"] . "!\n";
                exit(0);
            }
			include_once($plane["config"]);
			break;
		}
	}
	if (!$found)
	{
		print "Plane:$plane_name is not defined in in vars.inc file!\n";
		exit(0);
	}
	print_top($fd);
	fputs($fd,"\t" . '$("h4#date").text("Last Updated: "+Date());' . "\n");
	$no = 1;
	foreach ($widgets_array as $row)
	{
		foreach ($row as $widget)
		{
			$div = 'DIV_' . $no;
			if($widget["TYPE"] == "chart")
			{
				switch ($widget["SIZE"])
				{
					case 'small':
						fputs($fd,"\t" . "$('#" . $div . "').addClass('small_div_class');" . "\n");
						break;
					case 'medium':
						fputs($fd,"\t" . "$('#" . $div . "').addClass('med_div_class');" . "\n");
						break;
					case 'big':
						fputs($fd,"\t" . "$('#" . $div . "').addClass('big_div_class');" . "\n");
						break;
				}
				fputs($fd,"\t" . "$('#" . $div . "').show();" . "\n");
				fputs($fd,"\t" . $widget['NAME'] . "_" . "submit(ROOT,'" . $div . "','#form_" . $widget['NAME'] . "','#click_" . $widget['NAME'] . "');" . "\n");
				fputs($fd,"\t" . "$('#form_" . $widget['NAME'] . "').trigger('submit');" . "\n");
				fputs($fd,"\t" . '$("h4#date").text("Last Updated: "+Date());' . "\n");
